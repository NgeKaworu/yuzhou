import 'jest';

import cartesianProduct from './product';

describe('cartesianProduct test', () => {
  it('1, 2, 3 test', () => {
    expect(cartesianProduct(1, 2, 3)).toStrictEqual([[1, 2, 3]]);
  });
  it("[1, 2, 3], ['a', 'b', 'c'] test", () => {
    expect(cartesianProduct([1, 2, 3], ['a', 'b', 'c'])).toStrictEqual([
      [1, 'a'],
      [1, 'b'],
      [1, 'c'],
      [2, 'a'],
      [2, 'b'],
      [2, 'c'],
      [3, 'a'],
      [3, 'b'],
      [3, 'c'],
    ]);
  });
  it("[1, 2, 3], ['a', 'b', 'c'], ['!', '@', '#'] test", () => {
    expect(cartesianProduct([1, 2, 3], ['a', 'b', 'c'], ['!', '@', '#'])).toStrictEqual([
      [1, 'a', '!'],
      [1, 'a', '@'],
      [1, 'a', '#'],
      [1, 'b', '!'],
      [1, 'b', '@'],
      [1, 'b', '#'],
      [1, 'c', '!'],
      [1, 'c', '@'],
      [1, 'c', '#'],
      [2, 'a', '!'],
      [2, 'a', '@'],
      [2, 'a', '#'],
      [2, 'b', '!'],
      [2, 'b', '@'],
      [2, 'b', '#'],
      [2, 'c', '!'],
      [2, 'c', '@'],
      [2, 'c', '#'],
      [3, 'a', '!'],
      [3, 'a', '@'],
      [3, 'a', '#'],
      [3, 'b', '!'],
      [3, 'b', '@'],
      [3, 'b', '#'],
      [3, 'c', '!'],
      [3, 'c', '@'],
      [3, 'c', '#'],
    ]);
  });
  it("['a', 'b', 'c'], '!', [1, 2, 3] test", () => {
    expect(cartesianProduct(['a', 'b', 'c'], '!', [1, 2, 3])).toStrictEqual([
      ['a', '!', 1],
      ['a', '!', 2],
      ['a', '!', 3],
      ['b', '!', 1],
      ['b', '!', 2],
      ['b', '!', 3],
      ['c', '!', 1],
      ['c', '!', 2],
      ['c', '!', 3],
    ]);
  });
});
